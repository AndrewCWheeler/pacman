var score = 0;
var pacman = {
  x: 1,
  y: 1,
  direction: "",
};

function displayWorld() {
  var output = "";

  for (var i = 0; i < world.length; i++) {
    output += "<div class='row'>";
    for (var j = 0; j < world[i].length; j++) {
      if (world[i][j] == 2) output += "<div class='brick'></div>";
      else if (world[i][j] == 1) output += "<div class='coin'></div>";
      else if (world[i][j] == 3) output += "<div class='cherries'></div>";
      if (world[i][j] == 0) output += "<div class='empty'></div>";
    }
    output += "</div>";
  }
  // console.log(output);
  document.getElementById("world").innerHTML = output;
}
function displayPacman() {
  document.getElementById("pacman").style.top = pacman.y * 20 + "px";
  document.getElementById("pacman").style.left = pacman.x * 20 + "px";
}
function displayScore() {
  document.getElementById("score").innerHTML = score;
}
displayWorld();
displayPacman();
displayScore();

function updatePacman() {
  document.getElementById("pacman").style.top = pacman.y * 20 + "px";
  document.getElementById("pacman").style.left = pacman.x * 20 + "px";
  document.getElementById("pacman").style.backgroundImage =
    "url('css/img/pacman" + direction + ".gif')";
}

document.onkeydown = function (e) {
  // to stop at walls...
  if (e.keyCode == 37 && world[pacman.y][pacman.x - 1] != 2) {
    // to teleport to other side of world...
    if (pacman.x == 0) {
      pacman.x = world[0].length;
    }
    pacman.x--;
    direction = "left";
  } else if (e.keyCode == 39 && world[pacman.y][pacman.x + 1] != 2) {
    if (pacman.x == world[0].length - 1) {
      pacman.x = -1;
    }
    pacman.x++;
    direction = "right";
  } else if (e.keyCode == 38 && world[pacman.y - 1][pacman.x] != 2) {
    if (pacman.y == 0) {
      pacman.y = 18;
    }
    pacman.y--;
    direction = "up";
  } else if (e.keyCode == 40 && world[pacman.y + 1][pacman.x] != 2) {
    pacman.y++;
    direction = "down";
  }

  // to "eat" coins and count points
  if (world[pacman.y][pacman.x] == 1) {
    world[pacman.y][pacman.x] = 0;
    score += 10;
    displayWorld();
    displayScore();
  }

  // to "eat" cherries and count points
  if (world[pacman.y][pacman.x] == 3) {
    world[pacman.y][pacman.x] = 0;
    score += 50;
    displayWorld();
    displayScore();
  }

  // displayPacman();
  updatePacman();
  console.log(pacman);
};

